library(shiny)
library(DT)

# Define server logic required to draw a histogram
shinyServer(function(input, output) {
  
  output$result_dt <- renderDT({
    datatable(iris)
  })
  
  output$map <- renderPlot({
    ggplot(iris , aes(x = Petal.Width, y = Sepal.Width)) + geom_point()
  })

})
